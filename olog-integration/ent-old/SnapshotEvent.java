package org.openepics.discs.hourlog.ent;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author vuppala
 */
@Entity
@Table(name = "snapshot_event")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "SnapshotEvent.findAll", query = "SELECT s FROM SnapshotEvent s"),
    @NamedQuery(name = "SnapshotEvent.findById", query = "SELECT s FROM SnapshotEvent s WHERE s.id = :id")})
public class SnapshotEvent implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
    @NotNull
    @Column(name = "id")
    private Integer id;
    @JoinColumn(name = "event", referencedColumnName = "event_id")
    @ManyToOne(optional = false)
    private Event event;
    @JoinColumn(name = "snapshot", referencedColumnName = "snapshot_id")
    @ManyToOne(optional = false)
    private Snapshot snapshot;

    public SnapshotEvent() {
    }

    public SnapshotEvent(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Event getEvent() {
        return event;
    }

    public void setEvent(Event event) {
        this.event = event;
    }

    public Snapshot getSnapshot() {
        return snapshot;
    }

    public void setSnapshot(Snapshot snapshot) {
        this.snapshot = snapshot;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof SnapshotEvent)) {
            return false;
        }
        SnapshotEvent other = (SnapshotEvent) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "org.openepics.discs.hourlog.ent.SnapshotEvent[ id=" + id + " ]";
    }
    
}
